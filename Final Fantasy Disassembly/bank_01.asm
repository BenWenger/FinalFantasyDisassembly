;;
;;  Mini Map Decompress  [$BF40 :: 0x07F50]
;;
;;    This is the stupidest, biggest waste of code and space I can think of.
;;  This is basically another version of DecompressMap... only instead of 
;;  decompressing the map to a given pointer... it decompresses to hardcoded
;;  addresses.  What's more... this routine appears to be COPY/PASTED again
;;  with only minor address changes done the second time!
;;
;;    The ONLY thing I can think of as to why the behavior might be this way
;;  is that this routine would be slightly faster due to the Absolute,X write
;;  instead of Indirect,Y write.  Since the game needs to decompress 2 rows
;;  every frame on top of doing a few other things... maybe that speed boost makes
;;  a difference.  I really don't buy that, though... I mean Abs,X is only 1
;;  cycle faster than Ind,Y -- given that there's 512 writes, this would mean
;;  the routine only shaves 512 cycles, which is less than 5 scanlines -- not even 5%
;;  of the total frame time.
;;
;;    And even if that were the case... such minor speed boosts wouldn't be necessary
;;  if the minimap drawing routine wasn't so CRIMINALLY inefficient.  See my notes
;;  there for rants.
;;
;;    C'est la vie.  Since I already commented DecompressMap thoroughly,
;;  I don't feel compelled to do the same here, so I just put in label and
;;  variable names and did some indenting to make the flow more clear -- but
;;  comments are sparse.
;;
;;  IN:    mm_maprow = desired rows to decompress (this row and the row after it
;;                               are decompressed)
;;
;;  OUT:   mm_mapbuf = buffer to receive decompressed row
;;        mm_mapbuf2 = receives second decompressed row (minimap_row+1)
;;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

MinimapDecompress:
    LDA mm_maprow
    ASL A
    TAX
    BCC @LowHalf

      LDA lut_OWPtrTbl + $100, X
      STA tmp
      LDA lut_OWPtrTbl + $101, X
      STA tmp+1
      JMP @Start

  @LowHalf:
      LDA lut_OWPtrTbl, X
      STA tmp
      LDA lut_OWPtrTbl+1, X
      STA tmp+1

  @Start:                ; here (tmp) points to compressed row
    LDY #0
    LDX #0

  @Loop:
    LDA (tmp), Y         ; get tile
    BPL @SingleTile

    CMP #$FF
    BEQ DoNextRow        ; look for termination code

    AND #$7F             ; otherwise it's a run
    STA tmp+2
    INC tmp
    BNE :+
      INC tmp+1
:   LDA (tmp), Y
    TAY
    DEY
    LDA tmp+2

  @RunLoop:
    STA mm_mapbuf, X
    INX
    DEY
    BNE @RunLoop
    LDY #0

  @SingleTile:
    STA mm_mapbuf, X
    INX
    INC tmp
    BNE @Loop
    INC tmp+1
    JMP @Loop

  DoNextRow:         ; didn't make this local because I wanted to be able to use the
                     ;   same local labels in this half (since it is just a copy/paste)
                     ;  following this is just a copy/paste of above (I'm not joking -- maybe
                     ;  it was a macro or something -- either way that's stupid).  Only thing
                     ;  different is it adds 1 to minimap_row, and writes to mm_mapbuf2.
                     ;  and of course it @Exit's rather than jumping to DoNextRow

    LDA mm_maprow
    CLC
    ADC #1
    ASL A
    TAX
    BCC @LowHalf

      LDA lut_OWPtrTbl + $100, X
      STA tmp
      LDA lut_OWPtrTbl + $101, X
      STA tmp+1
      JMP @Start

  @LowHalf:
      LDA lut_OWPtrTbl, X
      STA tmp
      LDA lut_OWPtrTbl+1, X
      STA tmp+1

  @Start:                ; here (tmp) points to compressed row
    LDY #0
    LDX #0

  @Loop:
    LDA (tmp), Y         ; get tile
    BPL @SingleTile

    CMP #$FF
    BEQ @Exit            ; look for termination code

    AND #$7F             ; otherwise it's a run
    STA tmp+2
    INC tmp
    BNE :+
      INC tmp+1
:   LDA (tmp), Y
    TAY
    DEY
    LDA tmp+2

  @RunLoop:
    STA mm_mapbuf2, X
    INX
    DEY
    BNE @RunLoop
    LDY #0

  @SingleTile:
    STA mm_mapbuf2, X
    INX
    INC tmp
    BNE @Loop
    INC tmp+1
    JMP @Loop

  @Exit:
    RTS

  ; Unused space

